
package oopinheritance;

public class Vehicle {
    public String engin;
    public String drivingSit;

    public Vehicle() {
    }

    public Vehicle(String engin, String drivingSit) {
        this.engin = engin;
        this.drivingSit = drivingSit;
    }

    public String getEngin() {
        return engin;
    }

    public void setEngin(String engin) {
        this.engin = engin;
    }

    public String getDrivingSit() {
        return drivingSit;
    }

    public void setDrivingSit(String drivingSit) {
        this.drivingSit = drivingSit;
    }
    
    
    
    
}
